# Это пример первой программы на Python.
# Она выводит на экран приветственное сообщение "Hello, World!".
# В комментариях объясняется назначение кода.

def main():
    """
    Главная функция программы.
    
    Функция выводит строку "Hello, World!" в консоль.
    """
    print("Hello, World!")  # вывод текста на экран


# Точка входа в программу.
# Код в этом блоке выполнится только если файл запущен напрямую.
if __name__ == "__main__":
    main()
